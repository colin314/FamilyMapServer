package DaoObjects;

public class UserDao {
    public UserDao() {
    }

    /**
     * Unique user name
     */
    public String Username;
    /**
     * Unique Person ID assigned to this user's generated Person object
     */
    public String Person_ID;
    /**
     * User's password
     */
    public String User_Password;
    /**
     * User's email address
     */
    public String Email;
}
